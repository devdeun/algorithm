/**
 * @param {number[]} nums
 * @param {number} k
 * @return {number[]}
 */
var topKFrequent = function(nums, k) {
    const map = new Map()    

    for (let i = 0; i < nums.length; i++) {
        map.set(nums[i], (map.get(nums[i]) || 0) + 1)
    }

    const sorted = [...map].sort((a, b) => b[1] - a[1])
    return sorted.slice(0, k).map(arr => arr[0])
};